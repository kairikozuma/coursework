%% 4.3 Matching Unknown Spectrograms
f4 = 4000;
tt = 0:1/f4:5.04;
cosA = cos(2*pi*1500*tt) + cos(2*pi*500*tt);
cosB = cos(2*pi*(250*tt+125*tt.*tt));
cosC = cos(2*pi*((1000/(3*pi))*sin(((3*pi)/2)*tt-(3*pi)/2)+1000*tt));
cosD = cos(1000*pi/log(2)*2.^(tt/2));
cosE = cos(2*pi*(1000*tt - 250*tt.*tt));
cosF = cos(2*pi*(250*tt.*tt)) + cos(2*pi*(-250*tt.*tt+1000*tt));
figure
subplot(3,2,1);
plotspec(cosA + j*1e-12, f4, 256);
%plot(tt, cosA);
title('Signal A');
xlabel('Time (seconds)');
ylabel('Frequency (Hz)');
subplot(3,2,2);
plotspec(cosB + j*1e-12, f4, 256);
%plot(tt, cosB);
title('Signal B');
xlabel('Time (seconds)');
ylabel('Frequency (Hz)');
subplot(3,2,3);
plotspec(cosC + j*1e-12, f4, 256);
%plot(tt, cosC);
title('Signal C');
xlabel('Time (seconds)');
ylabel('Frequency (Hz)');
